def crop(image_path, coords):
    """
    @param image_path: The path to the image to edit
    @param coords: A tuple of x/y coordinates (x1, y1, x2, y2)
    """
    image_obj = Image.open(image_path)
    return image_obj.crop(coords)

def detect_faces(img, draw_box=True):
  # convert image to grayscale
  grayscale_img = cv2.cvtColor(img, cv2.COLOR_BGR2GRAY)
  # detect faces
  faces = FACE_CLASSIFIER.detectMultiScale(grayscale_img, scaleFactor=1.6, minSize=(96,96))
  
  if len(faces) == 0:
    return "","",""
  else:
   # draw bounding box around detected faces
    for (x, y, width, height) in faces:
      if draw_box:
        cv2.rectangle(img, (x, y), (x+width, y+height), (0, 255, 0), 3)
    face_box = img[y:y+height, x:x+width]
    face_coords = [x,y,width,height]
    return img, face_box, face_coords

